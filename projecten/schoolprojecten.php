      <p class="Text">
        Boter kaas en eieren,ook wel "oo maal oo" of "kruisje rondje", is een eenvoudig spel voor twee spelers. Boter-kaas-en-eieren is in het Nederlands verreweg de meest gebruikte naam voor het spel. Het spel wordt met een potlood of met een pen op papier gespeeld. Het spel is ook op een computer te spelen. Bordspellen bestaan ook, maar deze hebben vaak meer een decoratief doel.
        Boter-kaas-en-eieren wordt gespeeld op 3 bij 3 velden. Bij het begin zijn alle velden leeg. De ene speler zet een 'kruis' en de andere speler een 'rondje'. Degene die drie van zijn eigen tekens op een rij heeft (diagonaal, verticaal of horizontaal), heeft gewonnen.
        <br>

        Iedereen moest voor zich zelf een bedrijf kiezen waarvoor zij boter kaas en eieren ging maken.
        <br>
        Ik koos voor de Jumbo Huizen.
        <br>
        Ik werkte alleen aan in dit product.
